package com.example.speedgun;

public class PositionCalculator {
    private int x;
    private int y;
    public PositionCalculator(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    public int getX()
    {
        return x;
    }
    public int getY()
    {
        return y;
    }
}
